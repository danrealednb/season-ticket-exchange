export interface SECTION {
  section: string;
  rows: Array<number | string>;
  seats: number;
  suite: boolean;
  chaseBridge: boolean;
  seatMap: string;
}

export const seatMap = (sectionId: string) => {
  const seatMap = venue.filter(
    (section: SECTION) => section.section === sectionId
  )[0].seatMap;
  return `https://www.msg.com/madison-square-garden/seating/msg-rangers/${seatMap}`;
};

export const getSectionInfo = (sectionId: string) => {
  const sectionData = venue.filter(
    (section: SECTION) => section.section === sectionId
  )[0];
  return sectionData;
};

export const getSectionRows = (sectionId: string) => {
  const sectionRowData = venue.filter(
    (section: SECTION) => section.section === sectionId
  )[0];
  let rowsStrArr = [];
  for (let index = 0; index < sectionRowData.rows.length; index++) {
    const row = sectionRowData.rows[index];
    if (typeof row === "number") {
      const rowStr = row.toString();
      rowsStrArr.push(rowStr);
    } else {
      rowsStrArr.push(row);
    }
  }
  return rowsStrArr;
};

export const getRowSeats = (sectionId: string) => {
  return venue.filter((section: SECTION) => section.section === sectionId)[0]
    .seats;
};
